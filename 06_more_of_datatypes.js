/* JavaScript is a dynamicallt type language -> This means
you do not need to explicity declare the data type of a 
varible, and the type of a variable can change at runtime
*/

const hero = ["Ironman", "captain America", "Thor"]
let myObj = {
    name : "Manav",
    age : 15
}

const myFunction = function(){
    console.log("Hello World")
}

console.log(typeof name);       // undefined
console.log(typeof age);        // undefined
console.log(typeof myObj);      // object
console.log(typeof muFunction); // undefined
console.log(typeof hero);       // object


